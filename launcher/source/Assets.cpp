GXTexObj Assets::GetTexture(const char* path, f32* width, f32* height)
{
    GXTexObj texObj = {};
    TPLFile tplFile = {};

    if (path == nullptr) {
        return texObj;
    }

    if (std::strcmp(path, "wfc-logo.tpl") == 0) {
        TPL_OpenTPLFromMemory(&tplFile, s_logoTexture, sizeof(s_logoTexture));
        TPL_GetTexture(&tplFile, 0, &texObj);
        if (width != nullptr || height != nullptr) {
            u32 fmt;
            u16 w, h;
            TPL_GetTextureInfo(&tplFile, 0, &fmt, &w, &h);
            if (width != nullptr) {
                *width = w;
            }
            if (height != nullptr) {
                *height = h;
            }
        }
    }

    return texObj;
}
